<?php namespace Modules\Projects\Providers;

use Illuminate\Support\ServiceProvider;

class ProjectsServiceProvider extends ServiceProvider {

	/**
	 * Indicates if loading of the provider is deferred.
	 *
	 * @var bool
	 */
	protected $defer = false;

	/**
	 * Boot the application events.
	 * 
	 * @return void
	 */
	public function boot()
	{
		$this->registerTranslations();
		$this->registerConfig();
		$this->registerViews();
	}

	/**
	 * Register the service provider.
	 *
	 * @return void
	 */
	public function register()
	{
		$this->app->bind(
			'Modules\Projects\Contracts\ImdbImporter',
			'Modules\Projects\Services\ImdbImporter\OmdbApi'
		);
		$this->app->bind(
            'Modules\Projects\Contracts\SubtitlesApi',
            'Modules\Projects\Services\SubtitlesApi\Opensubtitles'
        );
        $this->app->bind(
            'Captioning\File',
            'Modules\Projects\Extended\CaptioningCaptioning\SubripFile'
        );
	}

	/**
	 * Register config.
	 * 
	 * @return void
	 */
	protected function registerConfig()
	{
//		$this->publishes([
//		    __DIR__.'/../Config/config.php' => config_path('projects.php'),
//		]);
		$this->mergeConfigFrom(
		    __DIR__.'/../Config/config.php', 'projects'
		);
	}

	/**
	 * Register views.
	 * 
	 * @return void
	 */
	public function registerViews()
	{
//		$viewPath = base_path('resources/views/modules/projects');

		$sourcePath = __DIR__.'/../Resources/views';

//		$this->publishes([
//			$sourcePath => $viewPath
//		]);

//		$this->publishes([
//			__DIR__ . '/../storage/' => storage_path(),
//		]);

		$this->loadViewsFrom(array_merge(array_map(function ($path) {
			return $path . '/modules/projects';
		}, \Config::get('view.paths')), [$sourcePath]), 'projects');
	}

	/**
	 * Register translations.
	 * 
	 * @return void
	 */
	public function registerTranslations()
	{
		$langPath = base_path('resources/lang/modules/projects');

		if (is_dir($langPath)) {
			$this->loadTranslationsFrom($langPath, 'projects');
		} else {
			$this->loadTranslationsFrom(__DIR__ .'/../Resources/lang', 'projects');
		}
	}

	/**
	 * Get the services provided by the provider.
	 *
	 * @return array
	 */
	public function provides()
	{
		return array();
	}

}
